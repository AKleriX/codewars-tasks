function decode(str) {
    const morse = {
      '.-': 'a', '....': 'h', '---': 'o', '..-': 'u', '.----': '1', '-....': '6',
      '-...': 'b', '..' : 'i', '.--.': 'p', '...-': 'v', '..---': '2', '--...': '7',
      '-.-.': 'c', '.---': 'j', '--.-': 'q', '.--': 'w', '...--': '3', '---..': '8',
      '-..': 'd', '-.-': 'k', '.-.': 'r', '-..-': 'x', '....-': '4', '----.': '9',
      '.': 'e', '.-..': 'l', '...': 's', '-.--': 'y', '.....': '5', '-----': '0',
      '..-.': 'f', '--': 'm', '-': 't', '--..': 'z', '--.': 'g', '-.': 'n'    
    };
    return str.split('  ').map(w => w.split(' ').map(s => morse[s]).join('')).join(' ');
  }